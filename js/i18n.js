let currentLang = localStorage.getItem('ubuntu_sim_lang') || 'ru';

export function t(key) {
    return translations[currentLang]?.[key] || translations['en']?.[key] || key;
}

export function getLang() {
    return currentLang;
}

export function setLang(lang) {
    currentLang = lang;
    localStorage.setItem('ubuntu_sim_lang', lang);
}

export function toggleLang() {
    setLang(currentLang === 'ru' ? 'en' : 'ru');
    return currentLang;
}

// Resolve bilingual { ru: "...", en: "..." } objects to current language string
export function localized(value) {
    if (typeof value === 'string') return value;
    if (value && typeof value === 'object') {
        return value[currentLang] || value.en || '';
    }
    return '';
}

// Update all elements with data-i18n attribute
export function updatePageTranslations() {
    document.querySelectorAll('[data-i18n]').forEach(el => {
        const key = el.getAttribute('data-i18n');
        el.textContent = t(key);
    });

    // Update special elements
    const welcomeTitle = document.getElementById('welcome-title');
    if (welcomeTitle) welcomeTitle.textContent = t('welcome_title');

    const welcomeSubtitle = document.getElementById('welcome-subtitle');
    if (welcomeSubtitle) welcomeSubtitle.textContent = t('welcome_subtitle');

    // Update lang toggle switch
    const langSwitch = document.getElementById('lang-toggle');
    if (langSwitch) {
        langSwitch.setAttribute('data-active', currentLang);
        langSwitch.querySelectorAll('.lang-option').forEach(opt => {
            opt.classList.toggle('active', opt.dataset.lang === currentLang);
        });
    }
}
